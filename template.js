const conditionalSwitch = () => {
    let isMuul = 0;
    switch (isMuul) {
        case 0:
            console.log("Sıfır");
            break;
        case 1:
            console.log("Bir");
            break;
        case 2:
            console.log("İki");
            break;
        default:
            console.log("Bilinmiyor");
            break;
    }
}
